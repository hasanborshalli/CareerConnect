        <h2>What is the purpose of this platform?</h2>
        <p>This platform connects job seekers with companies. Job seekers can apply for jobs, and companies can post job listings and view applications from job seekers.</p>

        <h2>How do I sign up as a job seeker?</h2>
        <p>To sign up as a job seeker, click the "Sign Up" button on the homepage, select "Job Seeker" as your user type, and fill in the required information. After completing the registration, you can log in and start browsing job listings.</p>

        <h2>How do I apply for a job?</h2>
        <p>Once logged in as a job seeker, browse the available job listings. Each job post has an "Apply" button. Click it, and your application will be sent directly to the company posting the job.</p>

        <h2>How can I update my job seeker profile?</h2>
        <p>You can update your profile by logging in, going to your dashboard, and clicking the pencil icon in the section you want to update. Here, you can update your contact details, resume, and other relevant information.</p>

        <h2>How do I sign up as a company?</h2>
        <p>To sign up as a company, click the "Sign Up" button on the homepage, select "Company" as your user type, and provide the necessary details about your company. After registration, you can log in to start posting job listings.</p>

        <h2>How do I post a job as a company?</h2>
        <p>After signing in as a company, you can post a job by clicking on the "Post a Job" button in your dashboard. Fill in the job details, including the job title, description, requirements, and location, and then submit the listing.</p>

        <h2>How can I view applications for my posted jobs?</h2>
        <p>As a company, you can view applications by logging into your dashboard. There, you'll see a list of all jobs you've posted along with the number of applications received for each job. You can click on a job title to view the applicants' details and resumes.</p>

        <h2>Can I edit or delete a job posting?</h2>
        <p>Yes, you can edit or delete a job posting. Go to your dashboard, find the job listing you wish to modify, and select the "Edit" or "Delete" option.</p>

        <h2>Can I cancel or withdraw my application as a job seeker?</h2>
        <p>Yes, you can withdraw your application at any time before it is reviewed by the company. If you need to cancel your application, please contact the company directly for more information.</p>
        
        <h2>Is the platform free to use for both job seekers and companies?</h2>
        <p>Yes, the platform is free for both job seekers and companies to use. There may be premium features available for companies in the future, but basic job postings and job applications are completely free.</p>

        <h2>How do I report a problem or issue on the platform?</h2>
        <p>If you encounter any issues or have concerns, you can contact our support team via the "Contact Us" page. We will assist you as quickly as possible.</p>
